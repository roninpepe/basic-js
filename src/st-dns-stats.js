/**
 * Given an array of domains, return the object with the appearances of the DNS.
 *
 * @param {Array} domains
 * @return {Object}
 *
 * @example
 * domains = [
 *  'code.yandex.ru',
 *  'music.yandex.ru',
 *  'yandex.ru'
 * ]
 *
 * The result should be the following:
 * {
 *   '.ru': 3,
 *   '.ru.yandex': 3,
 *   '.ru.yandex.code': 1,
 *   '.ru.yandex.music': 1,
 * }
 *
 */
export default function getDNSStats(domains) {
  let r={}
  domains.forEach(e=>{for(let i of e.split(".").reverse().keys()){
    let s="."+e.split(".").reverse().slice(0,i+1).join(".")
    if(!r[s])r[s]=0
    r[s]++
  }})
  return r
}
